# ====================================================================
# TESTS PACKAGE
#
# This folder contains "automated tests" — small pieces of code that
# CHECK whether your real code works correctly.
#
# ANALOGY: If your code is a bridge, tests are the engineers who
# drive trucks across it to make sure it holds up. You write the
# bridge (the real code), then you write tests that try to break it.
# If the tests pass, you can be confident the code works.
#
# WHY BOTHER?
# When you change one part of the code, tests catch if you
# accidentally broke something else. Without tests, bugs hide
# until the worst possible moment (like when you're presenting
# your report to a payer executive).
#
# We'll add test files here as we build out the real code.
# ====================================================================
